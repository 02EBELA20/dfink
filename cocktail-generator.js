// კოქტეილების დალაგება ქართული ალფაბეტის მიხედვით
cocktails.sort((a, b) => a.name.localeCompare(b.name, 'ka-GE'));

// ცვლადები "მეტის ნახვა" ფუნქციონალისთვის
let displayedCocktails = 20; // თავდაპირველად 20 კოქტეილი
const cocktailsPerLoad = 40; // "მეტის ნახვა"-ზე 40 კოქტეილი

// კოქტეილების ჩვენება
function displayCocktails(cocktailsToShow, limit = displayedCocktails) {
    cocktailList.innerHTML = "";
    const cocktailsToDisplay = cocktailsToShow.slice(0, limit);
    
    cocktailsToDisplay.forEach(cocktail => {
        const card = document.createElement("div");
        card.classList.add("cocktail-card");
        card.innerHTML = `
          
            <h3>${cocktail.name}</h3>
            <p>${cocktail.description}</p>
        `;
        card.addEventListener("click", () => showModal(cocktail));
        cocktailList.appendChild(card);
    });

    // "მეტის ნახვა" ღილაკის მართვა
    if (cocktailsToShow.length > limit) {
        loadMoreBtn.style.display = "block";
    } else {
        loadMoreBtn.style.display = "none";
    }
}

// მოდალის ჩვენება
function showModal(cocktail) {
    modalImage.src = cocktail.image;
    modalTitle.textContent = cocktail.name;
    modalDescription.textContent = cocktail.description;

    // ინგრედიენტები
    modalIngredients.innerHTML = "";
    cocktail.ingredients.forEach(ingredient => {
        const li = document.createElement("li");
        li.textContent = ingredient;
        modalIngredients.appendChild(li);
    });

    // დამზადების ნაბიჯები
    modalSteps.innerHTML = "";
    cocktail.steps.forEach(step => {
        const li = document.createElement("li");
        li.textContent = step;
        modalSteps.appendChild(li);
    });

    modalServing.textContent = cocktail.serving;
    modal.style.display = "block";
}

// მოდალის დახურვა
closeModal.addEventListener("click", () => {
    modal.style.display = "none";
});

// ფილტრაცია
function filterCocktails() {
    const searchTerm = searchBar.value.toLowerCase();
    const baseIngredient = baseIngredientFilter.value;
    const difficulty = difficultyFilter.value;

    const filteredCocktails = cocktails.filter(cocktail => {
        const matchesSearch = cocktail.name.toLowerCase().includes(searchTerm);
        const matchesBase = baseIngredient ? cocktail.baseIngredient === baseIngredient : true;
        const matchesDifficulty = difficulty ? cocktail.difficulty === difficulty : true;
        return matchesSearch && matchesBase && matchesDifficulty;
    });

    // ფილტრაციის შემდეგაც დავალაგოთ ალფაბეტურად
    filteredCocktails.sort((a, b) => a.name.localeCompare(b.name, 'ka-GE'));

    displayCocktails(filteredCocktails, displayedCocktails);
}

// "მეტის ნახვა" ღილაკის მოვლენა
loadMoreBtn.addEventListener("click", () => {
    displayedCocktails += cocktailsPerLoad; // დავამატოთ 40 კოქტეილი
    filterCocktails(); // განვაახლოთ ჩვენება
});

// ფილტრების მოვლენები
searchBar.addEventListener("input", () => {
    displayedCocktails = 20; // ფილტრაციისას თავიდან 20-ით დავიწყოთ
    filterCocktails();
});
baseIngredientFilter.addEventListener("change", () => {
    displayedCocktails = 20; // ფილტრაციისას თავიდან 20-ით დავიწყოთ
    filterCocktails();
});
difficultyFilter.addEventListener("change", () => {
    displayedCocktails = 20; // ფილტრაციისას თავიდან 20-ით დავიწყოთ
    filterCocktails();
});

// თავდაპირველად ყველა კოქტეილის ჩვენება (პირველი 20)
displayCocktails(cocktails, displayedCocktails);